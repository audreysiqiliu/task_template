function switchCues(currentCue) {
  var newCue;
  if (currentCue == "DarkRed"){ newCue = "DarkOrange"; }
  else if (currentCue == "Navy") { newCue = "Green"; }
  else if (currentCue == "Navy") { newCue = "Green"; }
  else if (currentCue == "DarkOrange") { newCue = "DarkRed"; }
return newCue;
}

